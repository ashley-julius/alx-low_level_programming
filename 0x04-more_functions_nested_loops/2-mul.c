/* This program multiplies two numbers */
#include <stdio.h>
#include "main.h"
/**
 * mul- multiplies the value of numbers
 * @a: an int type paramter
 * @b: an int type parameter
 * Return: the product of @a and @b
 */
int mul(int a, int b)
{
	int product;

	product = (a * b);
	return (product);
}
